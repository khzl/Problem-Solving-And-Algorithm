#include <iostream>
using namespace std;
int main() {
	 
	short int FirstNumber, SecondNumber;
	cout << "Please Enter the FirstNumber:" << endl;
	cin >> FirstNumber;
	cout << "Please Enter the SecondNumber:" << endl;
	cin >> SecondNumber;
	cout << "FirstNumber + SecondNumber = " << FirstNumber + SecondNumber << endl;
	cout << "FirstNumber - SecondNumber = " << FirstNumber - SecondNumber << endl;
	cout << "FirstNumber * SecondNumber = " << FirstNumber * SecondNumber << endl;
	cout << "FirstNumber / SecondNumber = " << FirstNumber / SecondNumber << endl;
	cout << "FirstNumber % SecondNumber = " << FirstNumber % SecondNumber << endl;

	return 0;
}